#!/bin/bash

# Random 50 contribution days across Apr–Jul 2025
dates=(
  "2025-04-02" "2025-04-05" "2025-04-09" "2025-04-14" "2025-04-18"
  "2025-04-22" "2025-04-27" "2025-04-30" "2025-05-03" "2025-05-06"
  "2025-05-09" "2025-05-13" "2025-05-17" "2025-05-21" "2025-05-25"
  "2025-05-28" "2025-06-01" "2025-06-04" "2025-06-07" "2025-06-11"
  "2025-06-15" "2025-06-19" "2025-06-23" "2025-06-27" "2025-06-30"
  "2025-07-02" "2025-07-05" "2025-07-08" "2025-07-12" "2025-07-16"
  "2025-07-19" "2025-07-22" "2025-07-25" "2025-07-28" "2025-07-31"
  "2025-04-07" "2025-04-12" "2025-04-16" "2025-05-01" "2025-05-11"
  "2025-05-15" "2025-05-19" "2025-06-03" "2025-06-09" "2025-06-13"
  "2025-06-17" "2025-07-01" "2025-07-10" "2025-07-14" "2025-07-20"
)

for date in "${dates[@]}"; do
  # Random 1–3 commits per day
  num_commits=$(( (RANDOM % 3) + 1 ))

  for ((i=1; i<=num_commits; i++)); do
    echo "Commit on $date $i" >> log.txt
    GIT_AUTHOR_DATE="$date 12:$i:00" \
    GIT_COMMITTER_DATE="$date 12:$i:00" \
    git commit -a -m "Backfill commit $i on $date" --allow-empty
  done
done

git push origin main
